#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();
const sourceRoot = path.join(repoRoot, 'src/app/api');
const targetRoot = path.join(repoRoot, 'apps/api/app/api');
const HTTP_METHODS = [
  'GET',
  'POST',
  'PUT',
  'PATCH',
  'DELETE',
  'HEAD',
  'OPTIONS',
];
const CONFIG_EXPORTS = [
  'dynamic',
  'dynamicParams',
  'revalidate',
  'fetchCache',
  'runtime',
  'preferredRegion',
  'maxDuration',
];

function listRouteFiles(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const absolutePath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...listRouteFiles(absolutePath));
      continue;
    }
    if (entry.isFile() && entry.name === 'route.ts') {
      files.push(absolutePath);
    }
  }

  return files;
}

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function detectMethodExports(sourceFile) {
  const source = fs.readFileSync(sourceFile, 'utf8');

  return HTTP_METHODS.filter((symbol) => {
    const regex = new RegExp(
      `\\bexport\\s+(?:async\\s+function|function|const|let|var)\\s+${symbol}\\b`,
      'm'
    );
    return regex.test(source);
  });
}

function detectConfigExports(sourceFile) {
  const source = fs.readFileSync(sourceFile, 'utf8');
  const detected = [];

  for (const symbol of CONFIG_EXPORTS) {
    const regex = new RegExp(
      `export\\s+const\\s+${symbol}\\s*=\\s*([^;]+);`,
      's'
    );
    const match = source.match(regex);
    if (!match) {
      continue;
    }

    detected.push({
      symbol,
      expression: match[1].trim(),
    });
  }

  return detected;
}

function detectReexportedSymbols(sourceFile) {
  const source = fs.readFileSync(sourceFile, 'utf8');
  const matches = [];
  const regex = /export\s*{([^}]+)}\s*from\s*['"]([^'"]+)['"]/g;

  for (const match of source.matchAll(regex)) {
    const rawSymbols = match[1]
      .split(',')
      .map((part) => part.trim())
      .filter(Boolean)
      .map((part) => part.split(/\s+as\s+/i)[0]?.trim())
      .filter(Boolean);

    for (const symbol of rawSymbols) {
      matches.push({
        symbol,
        from: match[2],
      });
    }
  }

  return matches;
}

function main() {
  if (!fs.existsSync(sourceRoot)) {
    console.error(`Missing source API root: ${sourceRoot}`);
    process.exit(1);
  }

  fs.rmSync(targetRoot, { recursive: true, force: true });
  ensureDir(targetRoot);

  const routeFiles = listRouteFiles(sourceRoot);

  for (const sourceFile of routeFiles) {
    const relativePath = path.relative(sourceRoot, sourceFile);
    const targetFile = path.join(targetRoot, relativePath);
    const targetDir = path.dirname(targetFile);
    ensureDir(targetDir);

    const importPath = toPosix(
      path.relative(targetDir, sourceFile).replace(/\.ts$/, '')
    );
    const methodExports = detectMethodExports(sourceFile);
    const configExports = detectConfigExports(sourceFile);
    const reexportedSymbols = detectReexportedSymbols(sourceFile);
    const reexportLines = reexportedSymbols
      .filter(({ symbol }) => [...HTTP_METHODS, ...CONFIG_EXPORTS].includes(symbol))
      .map(({ symbol, from }) => `export { ${symbol} } from '${from}';`);

    const contents = [
      '// Auto-generated by scripts/generate-api-app-wrappers.mjs',
      ...methodExports.map((symbol) => `export { ${symbol} } from '${importPath}';`),
      ...configExports.map(
        ({ symbol, expression }) => `export const ${symbol} = ${expression};`
      ),
      ...reexportLines,
      '',
    ].join('\n');

    fs.writeFileSync(targetFile, contents, 'utf8');
  }

  console.log(`Generated ${routeFiles.length} API route wrappers in apps/api.`);
}

main();
